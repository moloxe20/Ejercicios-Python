# Definimos una función llamada 'saludar' que recibe un parámetro 'nombre'
def saludar(nombre):
    """
    Esta función saluda a la persona con el nombre dado.
    """
    # Usamos print() para mostrar el saludo
    print("Hola,", nombre)

# Llamamos a la función 'saludar' pasando "Ana" como argumento
saludar("Ana")

# Llamamos a la función 'saludar' pasando "Pedro" como argumento
saludar("Pedro")
