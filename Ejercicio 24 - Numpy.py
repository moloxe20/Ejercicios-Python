import numpy as np  # Importamos el módulo numpy para operaciones numéricas

# Crear un array NumPy
array = np.array([1, 2, 3, 4, 5])

# Operaciones básicas con arrays
print("Array original:", array)  # Imprimimos el array original
print("Array multiplicado por 2:", array * 2)  # Multiplicamos cada elemento por 2
print("Suma de todos los elementos del array:", np.sum(array))  # Sumamos todos los elementos del array
